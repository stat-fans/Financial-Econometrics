library(tidyverse)
#随机模拟多元线性回归
set.seed(123)
m <- tibble(
  x1=2*rnorm(1000),x2=rnorm(1000)/3,u=4*rnorm(1000),y=-1+7*x1+2*x2+u
) %>%
  lm(y~x1+x2,.) #.为占位符传递数据至data参数                                      

summary(m) #查看拟合模型的统计描述

